        async function fetchData() {
            try {
                const response = await fetch('/data/dliTV.json');
                const data = await response.json();
                renderContent(data);
            } catch (error) {
                console.error('Error fetching data:', error);
            }
        }

        function renderContent(data) {
            const mainContent = document.getElementById('main-content');
            let html = `<h1>${data.pageTitle}</h1><hr><p>${data.introduction}</p>`;

            data.sections.forEach(section => {
                html += `<h1>${section.title}</h1><p>${section.content}</p>`;
                if (section.source) {
                    html += `<p><i>${section.source}</i></p>`;
                }
            });

            html += `<h1>Tài nguyên thiên nhiên</h1>`;
            data.resources.forEach(resource => {
                html += `<p><strong>${resource.title}</strong></p><p>${resource.content}</p>`;
                if (resource.source) {
                    html += `<p><i>${resource.source}</i></p>`;
                }
            });

            mainContent.innerHTML = html;
        }

        document.addEventListener('DOMContentLoaded', fetchData);
